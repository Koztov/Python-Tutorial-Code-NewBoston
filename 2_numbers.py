from __future__ import division  # important module in division

print(2 + 3)
print(23 / 22)      # output will be in decimal due to the import
print(23 // 22)     # decimal can be ignored by using //
print(25 % 22)
print(3 * 3 * 3)
print(3 ** 3)       # use of power

num1 = 123
print(num1)         # prints a variable

num2 = 77
print(num1 / num2)  # operation of variables
